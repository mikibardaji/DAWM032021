public class Exemple1For {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int cont;
        System.out.println("el del for");
           //v.inicial;v_final;aumento 
        for (cont = 8; cont < 6; cont++) {
            System.out.println("numero " + cont);
            
        }
        cont=0;
        while(cont<=5)//while
        {
            System.out.println("numero " + cont);
            cont = cont+1;
        }
        cont=0;
        do{
          System.out.println("numero " + cont);
          cont++;
        }while(cont<6);
        
        
        
      
    }
    
}
